const User = require("../models/user.js");

module.exports.renderSignup = (req, res) => res.render('User/signup.ejs');

module.exports.signup = async (req, res, next) => {
    try {
        let { username, email, password } = req.body;
        const newUser = new User({ email, username });
        const registeredUser = await User.register(newUser, password);
        req.login(registeredUser, (err) => {
            if(err) return next(err);
            req.flash("success", "Welcome to Roavista!");
            res.redirect("/listings");
        });
    } catch(e) {
        req.flash("error", e.message); 
        res.redirect("/signup");
    }
};

module.exports.renderLogin = (req, res) => res.render('User/login.ejs');

module.exports.login = async (req, res) => {
    req.flash("success", "Welcome back to Roavista!");
    // If they were trying to go somewhere specific, send them there. Otherwise, send to /listings
    let redirectUrl = res.locals.redirectUrl || "/listings";
    res.redirect(redirectUrl);
};

module.exports.logout = (req, res, next) => {
    req.logout((err) => {
        if (err) { 
            return next(err); 
        }
        req.flash("success", "You have successfully logged out. Goodbye!");
        res.redirect("/listings");
    });
};

module.exports.renderProfile = (req, res) => {
    if(!req.user) {
        req.flash("error", "You must be logged in to view your profile.");
        return res.redirect("/login");
    }
    res.render("User/profile.ejs");
};
















































































































































































































































































































































































































































































































